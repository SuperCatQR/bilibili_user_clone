"""
全局配置常量模块

定义项目所有运行参数、路径和合法值集合。
所有常量在此集中管理，便于统一调优和维护。

设计原则：
- 请求间隔与重试策略：平衡下载速度与被限速风险
- 路径常量：支持环境变量覆盖，便于CI/CD和容器化部署
- 合法值集合：用于CLI参数校验，防止非法输入
"""

import os
from pathlib import Path

# ============================================================
# 请求控制参数
# ============================================================

# 请求间隔（秒），每次API调用或下载之间的等待时间
# 原理：B站API有频率限制，过快的请求会触发412状态码（限速）
# 3秒是一个经验值，在速度和安全之间取得平衡
DEFAULT_INTERVAL = 3

# API请求失败时的最大重试次数
# 网络抖动或临时性错误可以通过重试恢复
# 3次重试覆盖了绝大多数瞬时故障场景
DEFAULT_RETRY = 3

# 412限速指数退避的基础等待秒数
# 实际等待时间 = BACKOFF_BASE * 2^attempt
# 第0次: 5秒, 第1次: 10秒, 第2次: 20秒, ...
# 指数退避的目的是让服务器有时间恢复，避免雪崩效应
BACKOFF_BASE = 5

# 412退避等待上限（秒）
# 防止指数退避导致等待时间过长（理论上会达到 5*2^n）
# 300秒 = 5分钟，是一个合理的上限
BACKOFF_MAX = 300

# ============================================================
# 批量处理参数
# ============================================================

# 每处理多少项后触发一次阶梯暂停
# 50项是一个批次大小，在连续下载时提供喘息窗口
BATCH_SIZE = 50

# 阶梯暂停时长循环序列（秒）
# 每BATCH_SIZE项后按此序列循环暂停：5→10→15→20→5→10→...
# 递增暂停时间可以逐步降低服务器压力，循环避免暂停时间无限增长
BATCH_PAUSE_STEPS = [5, 10, 15, 20]

# ============================================================
# 文件下载参数
# ============================================================

# 文件下载分块大小（字节）
# 8KB = 8192字节，是操作系统页大小的整数倍
# 过小的chunk会增加系统调用次数，过大会占用过多内存
# 8KB在内存占用和IO效率之间取得平衡
CHUNK_SIZE = 8192

# 文件名最大长度（字符）
# 文件系统限制：NTFS=255, ext4=255, APFS=255
# 200字符留有余量，避免路径拼接后超过系统限制
MAX_FILENAME_LENGTH = 200

# ============================================================
# 凭据存储参数
# ============================================================

# 凭据有效期（天），超过后需重新扫码登录
# 7天是B站Cookie的典型有效期，过期后API调用会返回未登录状态
CREDENTIAL_TTL_DAYS = 7

# 凭据文件目录：~/.bilibili-cli/
# 使用用户主目录下的隐藏目录，遵循Unix惯例
# 多个项目可共享同一凭据文件
CREDENTIAL_DIR = Path.home() / ".bilibili-cli"

# 凭据文件完整路径
CREDENTIAL_FILE = CREDENTIAL_DIR / "credential.json"

# ============================================================
# 数据库存储参数
# ============================================================

# SQLite断点续传数据库目录
# 默认在项目目录下的 .bilibili-clone/ 隐藏目录
# 可通过环境变量 BILIBILI_CLONE_DB_DIR 覆盖，便于容器化部署
DB_DIR = Path(os.environ.get("BILIBILI_CLONE_DB_DIR", ".bilibili-clone"))

# 数据库文件完整路径
DB_FILE = DB_DIR / "downloads.db"

# ============================================================
# 合法值集合（用于CLI参数校验）
# ============================================================

# 合法的内容类型集合
# video: 视频（B站主站视频）
# audio: 音频（B站音频区）
# article: 专栏（B站专栏文章）
# dynamic: 动态（B站用户动态）
VALID_TYPES = {"video", "audio", "article", "dynamic"}

# 合法的视频下载模式集合
# full: 下载视频轨+音频轨，ffmpeg合流为mp4
# video-only: 仅下载视频轨（m4v格式）
# audio-only: 仅下载音频轨，转码为WAV
# subtitle-only: 仅下载字幕（SRT格式）
# none: 跳过下载，仅保存元数据
VIDEO_MODES = {"full", "video-only", "audio-only", "subtitle-only", "none"}
